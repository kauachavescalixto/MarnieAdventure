package database;

public class Users {
	
	private int id,galinha,pato,vaca,cabra,ovelha,dinheiro,login;
	private String nome;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getGalinha() {
		return galinha;
	}
	public void setGalinha(int galinha) {
		this.galinha = galinha;
	}
	public int getPato() {
		return pato;
	}
	public void setPato(int pato) {
		this.pato = pato;
	}
	public int getVaca() {
		return vaca;
	}
	public void setVaca(int vaca) {
		this.vaca = vaca;
	}
	public int getCabra() {
		return cabra;
	}
	public void setCabra(int cabra) {
		this.cabra = cabra;
	}
	public int getOvelha() {
		return ovelha;
	}
	public void setOvelha(int ovelha) {
		this.ovelha = ovelha;
	}
	public int getDinheiro() {
		return dinheiro;
	}
	public void setDinheiro(int dinheiro) {
		this.dinheiro = dinheiro;
	}
	public int getLogin() {
		return login;
	}
	public void setLogin(int login) {
		this.login = login;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}

}
